def game_loop(args):
    """
    Main loop of the simulation. It handles updating all the HUD information,
    ticking the agent and, if needed, the world.
    """

    pygame.init()
    pygame.font.init()
    world = None

    try:
        if args.seed:
            random.seed(args.seed)

        client = carla.Client(args.host, args.port)
        client.set_timeout(60.0)

        # check available maps
        available_maps = client.get_available_maps()
        print("Available maps: ", available_maps)
        
        # load a map 
        client.load_world('Town10HD')
        
        # start a recorder
        if args.enable_recording:   
            print("Recording on file: %s" % client.start_recorder(args.recorder_filename))


        # traffic manager
        traffic_manager = client.get_trafficmanager()
        # donot simulate traffic far away from the player
        traffic_manager.set_hybrid_physics_mode(True)
        traffic_manager.set_hybrid_physics_radius(70.0)
        traffic_manager.set_random_device_seed(0)
            
        sim_world = client.get_world()
        # if use synchronous mode and fixed simulation time step
        # then traffic manager has to be set to synchronous mode as well
        if args.sync:
            settings = sim_world.get_settings()
            settings.synchronous_mode = True
            # this decides the time step in server? but why my server fps is around 40 during simulation?
            settings.fixed_delta_seconds = 0.05 
            sim_world.apply_settings(settings)

            traffic_manager.set_synchronous_mode(True)

        # set up the traffic manager to introduce NPCs        
        spawn_traffic(client, traffic_manager, 10)
        
        # set up a display to render the game
        # pygame.HWSURFACE: hardware accelerated
        # pygame.DOUBLEBUF: use a double buffer for rendering
        display = pygame.display.set_mode(
            (args.width, args.height),
            pygame.HWSURFACE | pygame.DOUBLEBUF)

        # set up heads-up display info
        hud = HUD(args.width, args.height)
        world = World(client.get_world(), hud, args)

        # set up a keyboard controller to quit the game by pressing ESC/q
        controller = KeyboardControl(world)
        
        # set up an control agent
        if args.agent == "Basic":
            agent = BasicAgent(world.player, 20)
            agent.follow_speed_limits(True)
        elif args.agent == "Constant":
            agent = ConstantVelocityAgent(world.player, 30)
            ground_loc = world.world.ground_projection(world.player.get_location(), 5)
            if ground_loc:
                world.player.set_location(ground_loc.location + carla.Location(z=0.01))
            agent.follow_speed_limits(True)
        elif args.agent == "Behavior":
            agent = BehaviorAgent(world.player, behavior=args.behavior)

        # Set the agent destination
        spawn_points = world.map.get_spawn_points()
        destination = random.choice(spawn_points).location
        agent.set_destination(destination)

        # set up debug plot for pid controller
        init_traj = agent.get_local_planner()._waypoints_queue[0][0].transform
        xs = [init_traj.location.x]
        ys = [init_traj.location.y]
        xm = [world.player.get_location().x]
        ym = [world.player.get_location().y]

        fig1, ax1 = plt.subplots(figsize=(12,12))
        lns, = plt.plot(xs, ys, 'ro', markersize=1, label='target location')
        lnm, = plt.plot(xm, ym, 'b*', markersize=1, label='vehicle location') 

        fig2, ax2 = plt.subplots(4, 1, figsize=(12,12))
        vm = [get_speed(world.player)]
        vs = [agent.get_local_planner()._target_speed]
        lvm, = ax2[0].plot(range(len(vm)), vm, 'b-', lw=1, label='vehicle speed')
        lvs, = ax2[0].plot(range(len(vs)), vs, 'r-', lw=1, label='target speed')

        throttle = [0]
        lvc, = ax2[1].plot(range(len(throttle)), throttle, 'b-', lw=1, label='throttle')

        steer_error = [0]
        lsteer_err, = ax2[2].plot(range(len(steer_error)), steer_error, 'b-', lw=1, label='steer error')

        steer = [0]
        lsteer, = ax2[3].plot(range(len(steer)), steer, 'b-', lw=1, label='steer')


        clock = pygame.time.Clock()

        while True:
            clock.tick()
            if args.sync:
                world.world.tick()
            else:
                world.world.wait_for_tick()
            
            # if interupted by user, then quit the game
            if controller.parse_events():
                return

            world.tick(clock)
            world.render(display)

            # display next waypoints
            traj = agent.get_local_planner()._waypoints_queue
            
            traj_surface = pygame.Surface((args.width, args.height))
            traj_surface.set_colorkey((0, 0, 0))
            locations = []
            for i in range(min(20, len(traj))):
                transform = traj[i][0].transform
                locations.append(transform.location)

            for i in range(len(locations)-1):
                #carla.DebugHelper.draw_line(traj_surface, TRAJ_COLOR, points[i], points[i+1], 5)
                #world.world.debug.draw_line(locations[i], locations[i+1], color = carla.Color(255, 0, 0), life_time=0.1)
                world.world.debug.draw_point(locations[i], size=0.05, color = carla.Color(255, 0, 0), life_time=0.1)


            # plot motion planner trajectory
            if agent._local_planner._lateral_controller == "MPC":
                print("MPC trajectory plotting")
                lat_mpc = agent._local_planner._vehicle_controller._lat_controller
                mpc_traj_ref = lat_mpc._ref_traj
                mpc_traj_pred = lat_mpc._pred_traj
                mpc_traj_raw = lat_mpc._raw_traj
                if len(mpc_traj_ref) > 0:
                    print("goal in planner: ", lat_mpc._motion_planner._goal.transform)
                    print("goal in spiral: ", mpc_traj_raw.x[-1], mpc_traj_raw.y[-1], mpc_traj_raw.theta[-1], mpc_traj_raw.kappa[-1])
                #for idx in range(len(mpc_traj_raw)):
                #    li_raw = carla.Location(x=mpc_traj_raw[idx].path_point.x, y=mpc_traj_raw[idx].path_point.y, z=0)
                #    world.world.debug.draw_point(li_raw, size=0.1, color=carla.Color(255, 255, 0), life_time=0.1)

                for idx in range(len(mpc_traj_ref)):
                    li_ref = carla.Location(x=mpc_traj_ref[idx].path_point.x, y=mpc_traj_ref[idx].path_point.y, z=0.0)
                    li_pred = carla.Location(x=mpc_traj_pred[idx].path_point.x, y = mpc_traj_pred[idx].path_point.y, z=0)
                    world.world.debug.draw_point(li_ref, size=0.1, color=carla.Color(0, 255, 0), life_time = 0.1)
                    world.world.debug.draw_point(li_pred, size=0.1, color=carla.Color(0, 0, 255), life_time=0.1)

            display.blit(traj_surface, (0, 0))
            
            pygame.display.flip()


            # if the agent has reached the destination
            if agent.done():
                if args.loop:
                    agent.set_destination(random.choice(spawn_points).location)
                    world.hud.notification("Target reached", seconds=4.0)
                    print("The target has been reached, searching for another target")
                else:
                    print("The target has been reached, stopping the simulation")
                    break

            control = agent.run_step()
            control.manual_gear_shift = False
            world.player.apply_control(control)

            # update the debug plot
            xm.append(world.player.get_location().x)
            ym.append(world.player.get_location().y)
            if traj and len(traj) > 0:
                xs.append(traj[0][0].transform.location.x)
                ys.append(traj[0][0].transform.location.y)
            lns.set_data(xs, ys)
            lnm.set_data(xm, ym)
            ax1.legend()
            ax1.relim()
            ax1.autoscale_view()
            
            vm.append(get_speed(world.player))
            vs.append(agent.get_local_planner()._target_speed)
            throttle.append(control.throttle - control.brake)
            steer.append(world.player.get_control().steer)
            steer_error.append(agent._local_planner._vehicle_controller._lat_controller._e_buffer[-1])
            lvm.set_data(range(len(vm)), vm)
            lvs.set_data(range(len(vs)), vs)
            lvc.set_data(range(len(throttle)), throttle)
            lsteer_err.set_data(range(len(steer_error)), steer_error)
            lsteer.set_data(range(len(steer)), steer)

            ax2[0].legend()
            ax2[0].relim()
            ax2[0].autoscale_view()
            ax2[1].legend()
            ax2[1].relim()
            ax2[1].autoscale_view()
            ax2[2].legend()
            ax2[2].relim()
            ax2[2].autoscale_view()
            ax2[3].legend()
            ax2[3].relim()
            ax2[3].autoscale_view()
            plt.draw()
            plt.pause(0.001)    

    finally:

        if world is not None:
            settings = world.world.get_settings()
            settings.synchronous_mode = False
            settings.fixed_delta_seconds = None
            world.world.apply_settings(settings)
            traffic_manager.set_synchronous_mode(True)

            world.destroy()

            if args.enable_recording:
                print("Stop recording")
                client.stop_recorder()
            
            fig1.savefig('pid_control_traj.png')
            fig2.savefig('pid_control_speed.png')

        pygame.quit()


